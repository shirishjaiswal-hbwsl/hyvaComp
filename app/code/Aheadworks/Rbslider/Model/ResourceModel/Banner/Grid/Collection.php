declare(strict_types=1);

namespace Aheadworks\Rbslider\Model\ResourceModel\Banner\Grid;

use Aheadworks\Rbslider\Model\ResourceModel\Banner\Collection as BannerCollection;
use Magento\Framework\Api\Search\SearchResultInterface;
use Magento\Framework\Api\SearchCriteriaInterface;
use Magento\Framework\View\Element\UiComponent\DataProvider\Document;

/**
 * Class Collection
 * @package Aheadworks\Rbslider\Model\ResourceModel\Banner\Grid
 */
class Collection extends BannerCollection implements SearchResultInterface
{
    /**
     * Set relation to model and resource model
     *
     * @return void
     */
    public function _construct()
    {
        parent::_construct();
        $this->setModel(Document::class);
    }

    /**
     * {@inheritdoc}
     */
    public function getAggregations()
    {
        return null;
    }

    /**
     * {@inheritdoc}
     */
    public function setAggregations($aggregations)
    {
        return $this;
    }

    /**
     * {@inheritdoc}
     */
    public function getSearchCriteria()
    {
        return null;
    }

    /**
     * {@inheritdoc}
     * @SuppressWarnings(PHPMD.UnusedFormalParameter)
     */
    public function setSearchCriteria(SearchCriteriaInterface $searchCriteria = null)
    {
        return $this;
    }

    /**
     * {@inheritdoc}
     */
    public function getTotalCount()
    {
        return $this->getSize();
    }

    /**
     * {@inheritdoc}
     * @SuppressWarnings(PHPMD.UnusedFormalParameter)
     */
    public function setTotalCount($totalCount)
    {
        return $this;
    }

    /**
     * {@inheritdoc}
     * @SuppressWarnings(PHPMD.UnusedFormalParameter)
     */
    public function setItems(array $items = null)
    {
        return $this;
    }
}
